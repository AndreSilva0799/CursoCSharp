using System;

namespace PrimeiroProjeto
{
    internal class Program
    {
        static void Main(string[] args)
        {
            sbyte x = 100;

            Console.WriteLine("sbyte " + x);

            byte n1 = 126;

            Console.WriteLine("byte " + n1);

